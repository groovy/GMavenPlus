package org.codehaus.gmavenplus.model;


/**
 * Scope access modifiers supported by Java.
 *
 * @author Keegan Witt
 */
public enum Scopes {

    /**
     * The public scope.
     */
    PUBLIC,

    /**
     * The protected scope.
     */
    PROTECTED,

    /**
     * The package scope.
     */
    PACKAGE,

    /**
     * The private scope.
     */
    PRIVATE

}
